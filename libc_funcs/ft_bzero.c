#include "libft.h"

void		*ft_bzero(void *str1, size_t n)
{
	char	*reset;
	size_t	i;

	reset = str1;
	i = 0;
	while (reset[i] != '\0' && n != 0)
	{
		reset[i] = 0;
		i++;
		n--;
	}
	return (str1);
}
