#include "libft.h"

char		*ft_strcpy(char *str1,char *str2)
{
		int i;
		i = 0;
		while (str2[i] != '\0')
		{
			i++;
		}

		int j;
		j = 0;
		while (str1[j])
		{
			str1[j] = str2[j];
				j++;
		}
		return (str1);
}
