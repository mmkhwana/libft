#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	if (*s1 == '\0' || *s2 == '\0')
		return (1);
	if (s1 == s2)
	{
		while ((s1[i] && s2[i]) && (s1[i] == s2[i]))
		{
			i++;
		}
		return (1);
	}
	else
		return (0);
}
